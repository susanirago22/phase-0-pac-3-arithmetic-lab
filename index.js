function add(j, k)
{
    return (j + k);
}
function subtract(j, k)
{
    return (j- k);
}
function multiply(j, k)
{
    return (j* k);
}
function divide(j, k)
{
    return (j/k);
}
function increment(p)
{
    return (p += 1);
}
function decrement(p)
{
    return (p -= 1);    
}
function makeInt(p)
{
    return parseInt(p, 10);
}
function preserveDecimal(p)
{
    return parseFloat(p);
}
